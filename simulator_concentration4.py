import random
from random_concentration_generator import RandomConcentrationSimulator


class SimulatorConcentration4:
    def __init__(self):
        self.тренажер = RandomConcentrationSimulator()

    def начать_уровень(self):
        """Начинает уровень со случайными задачами"""
        return self.тренажер.начать_уровень()

    def получить_текущую_задачу(self):
        """Возвращает условие текущей задачи"""
        return self.тренажер.получить_текущую_задачу()

    def получить_следующую_задачу(self):
        """Возвращает следующую случайную задачу"""
        return self.тренажер.получить_следующую_задачу()

    def проверить_ответ(self, ответ_пользователя):
        """Проверяет ответ пользователя на текущую задачу"""
        return self.тренажер.проверить_ответ(ответ_пользователя)

    def получить_подсказку(self):
        """Возвращает подсказку для текущей задачи"""
        return self.тренажер.получить_подсказку()

    def показать_ответ(self):
        """Показывает правильный ответ для текущей задачи"""
        return self.тренажер.показать_ответ()